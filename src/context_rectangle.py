# -*- coding: utf-8 -*-
## context_rectangle ##

class context_rectangle(object):
    """\brief rectangle of context area
    """
    def __init__(self, x, y, width, height):
        """\brief 
        \param x - float
        \param y - float
        \param width - float
        \param height - float
        """
        self.x = x
        self.y = y
        self.width = width
        self.height = height
